package gov.healthit.chpl.auth.permission;

public class UserPermissionRetrievalException extends Exception {
	
	private static final long serialVersionUID = 1L;
	public UserPermissionRetrievalException() { super(); }
	public UserPermissionRetrievalException(String message) { super(message); }
	public UserPermissionRetrievalException(String message, Throwable cause) { super(message, cause); }
	public UserPermissionRetrievalException(Throwable cause) { super(cause); }
	

}
